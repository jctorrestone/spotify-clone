const gridContainerDiv = document.querySelector(".grid-container");
const recentSearchDiv = document.querySelector(".recent-search");
const browseAllDiv = document.querySelector(".browse-all");

browseAllDiv.innerHTML = browseAllItems.map((item)=>{
    return `<a class="grid-item" style="background-color:${item.backgroundColor};" href="${item.href}">
                <h3>${item.title}</h3>
                <img class="item-img" src="${item.artwork}" alt="${item.title}">
            </a>`;
}).join(" ");

function setGaps() {
    if(window.innerWidth > 973){
        gridContainerDiv.style.setProperty("gap", "24px");
        recentSearchDiv.style.setProperty("gap", "24px");
        browseAllDiv.style.setProperty("gap", "24px");
    }
    else if (window.innerWidth > 772){
        gridContainerDiv.style.setProperty("gap", "18px");
        recentSearchDiv.style.setProperty("gap", "18px");
        browseAllDiv.style.setProperty("gap", "18px");
    }
    else{
        gridContainerDiv.style.setProperty("gap", "12px");
        recentSearchDiv.style.setProperty("gap", "12px");
        browseAllDiv.style.setProperty("gap", "12px");
    }
}

function setColumnCount() {
    if(window.innerWidth > 1709){
        gridContainerDiv.style.setProperty("--column-count", 7);
        recentSearchDiv.style.setProperty("--column-count", 7);
        browseAllDiv.style.setProperty("--column-count", 7);
    }
    else if (window.innerWidth > 1504){
        gridContainerDiv.style.setProperty("--column-count", 6);
        recentSearchDiv.style.setProperty("--column-count", 6);
        browseAllDiv.style.setProperty("--column-count", 6);
    }
    else if (window.innerWidth > 1301){
        gridContainerDiv.style.setProperty("--column-count", 5);
        recentSearchDiv.style.setProperty("--column-count", 5);
        browseAllDiv.style.setProperty("--column-count", 5);
    }
    else if (window.innerWidth > 973){
        gridContainerDiv.style.setProperty("--column-count", 4);
        recentSearchDiv.style.setProperty("--column-count", 4);
        browseAllDiv.style.setProperty("--column-count", 4);
    }
    else if (window.innerWidth > 772){
        gridContainerDiv.style.setProperty("--column-count", 3);
        recentSearchDiv.style.setProperty("--column-count", 3);
        browseAllDiv.style.setProperty("--column-count", 3);
    }
    else{
        gridContainerDiv.style.setProperty("--column-count", 2);
        recentSearchDiv.style.setProperty("--column-count", 2);
        browseAllDiv.style.setProperty("--column-count", 2);
    }
}

onload = () => {
    setGaps();
    setColumnCount();
}

onresize = () => {
    setGaps();
    setColumnCount();
}